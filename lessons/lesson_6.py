def glo_1():
    abs = (-9)  # объемлющая переменная, потому что есть вложенная функция с локальной переменной
    def rt():
        abs = 'hello'  # локальная переменная по отношению к объемлющей, потому что функция вложенная
        print(abs, 'local')
    rt()
    print(abs, 'объемлющая')


abs = ('join')
print(abs, 'global')
glo_1()

