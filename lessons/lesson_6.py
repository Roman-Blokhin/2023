def glo_2():
    ab = (44)
    def rt():
        nonlocal ab  # если нужно поменять объемлющую переменную
        ab = 'roman'
        print(ab, 'local')
    rt()
    print(ab, 'объемлющая')


ab = ('join')
glo_2()